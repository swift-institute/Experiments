//
//  BashCompletionVisitor.swift
//  argv-parser-protocol-spike
//
//  Visitor over an `Argument.Schema` (via `Argument.Command`) that
//  produces a minimal bash completion script.
//
//  This is the other half of the P2 verification: a different visitor
//  walks the same Schema and emits a machine-readable artifact (a bash
//  completion function). Like the HelpVisitor, dispatch is static — each
//  `visit(...)` method runs at compile time on a typed schema node, with
//  no string-tag dispatch or reflection.
//
//  Coverage in this spike: option/flag completion. When the user types
//  `<command> --<Tab>`, the generated function suggests the union of
//  declared --options, --flags, and --help. Positional value completion
//  (e.g., filename completion) is deliberately out of scope for the
//  spike — it would require attaching a `CompletionSource` per positional,
//  which is a separate design extension.
//

/// A visitor that accumulates the option/flag long-names of a command,
/// then emits a minimal bash completion script.
public struct BashCompletionVisitor: Argument.Schema.Visitor {
    public typealias Failure = Never

    /// Long-names available to the completion script.
    @usableFromInline
    internal var optionNames: [String] = []

    public init() {}

    public mutating func visit<V: Sendable & Equatable>(
        positional: Argument.Positional<V>
    ) throws(Never) {
        // Positional value completion is out of scope for the spike.
        // The bash function still completes options/flags after a
        // positional has been entered.
        _ = positional
    }

    public mutating func visit<V: Sendable & Equatable>(
        option: Argument.Option<V>
    ) throws(Never) {
        optionNames.append(option.name)
    }

    public mutating func visit(flag: Argument.Flag) throws(Never) {
        optionNames.append(flag.name)
    }

    /// Render the accumulated option/flag names into a minimal bash
    /// completion script registered against `command.name`.
    ///
    /// The generated function uses `compgen -W` with the space-separated
    /// option list and `complete -F` to register it. `--help` is always
    /// appended.
    public func render(for command: Argument.Command) -> String {
        let allNames = optionNames + ["--help"]
        let wordList = allNames.joined(separator: " ")
        let funcName = "_\(command.name)_completion"

        var script = ""
        script += "#!/usr/bin/env bash\n"
        script += "# Auto-generated bash completion for `\(command.name)`.\n"
        script += "# Generated by BashCompletionVisitor over Argument.Schema.\n"
        script += "\n"
        script += "\(funcName)() {\n"
        script += "    local cur\n"
        script += "    cur=\"${COMP_WORDS[COMP_CWORD]}\"\n"
        script += "    if [[ \"$cur\" == --* ]]; then\n"
        script += "        COMPREPLY=( $(compgen -W \"\(wordList)\" -- \"$cur\") )\n"
        script += "        return 0\n"
        script += "    fi\n"
        script += "    COMPREPLY=()\n"
        script += "    return 0\n"
        script += "}\n"
        script += "complete -F \(funcName) \(command.name)\n"
        return script
    }
}
